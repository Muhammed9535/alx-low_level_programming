#include "3-calc.h"
#include <stdio.h>
/**
  * op_add - addition function
  * @a: first integer
  * @b: second int
  * Return: int
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtraction
  * @a: first integer
  * @b: second int
  * Return: int
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplication
  * @a: first int
  * @b: second int
  * Return: int
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - division
  * @a: first int
  * @b: second int
  * Return: int
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - modulo
  * @a: first int
  * @b: second int
  * Return: int
  */
int op_mod(int a, int b)
{
	return (a % b);
}




